    private void listarClientes() {
        Conexion conexion = new Conexion();
        conexion.connect();
        ArrayList<String> clientes = conexion.getClientes();
        modelo.clear();
        for (int i = 0; i < clientes.size(); i++) {
            modelo.addElement(clientes.get(i));;
        }

        lstClientes.setModel(modelo);
    }

