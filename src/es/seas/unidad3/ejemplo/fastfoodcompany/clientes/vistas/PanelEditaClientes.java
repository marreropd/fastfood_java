    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (lstClientes.getSelectedIndex() == -1) {
            jLabel1.setText("No hay un cliente seleccionado para editar");
        } else {
            String str = lstClientes.getSelectedValue().toString();
            String[] list = str.split(" ");
            acciones.editarCliente(list[0], list[1]);
            jLabel1.setText("Cliente Editado");
            listarClientes();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

